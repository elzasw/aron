package cz.aron.transfagent.peva.codelist;

public class Peva2ThematicEvidenceGroup {
	
	private final String name;
	
	private final String code;

	public Peva2ThematicEvidenceGroup(String name, String code) {
		super();
		this.name = name;
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public String getCode() {
		return code;
	}

}
